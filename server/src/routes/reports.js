async function platformSystemOverview(user, filters) {
  const params = filterParams(filters, user);
  const orgWhere = `(:organisation_id IS NULL OR o.id = :organisation_id) AND ${dateClause('o')}`;
  const referralWhere = `(:organisation_id IS NULL OR r.referring_organisation_id = :organisation_id OR r.receiving_organisation_id = :organisation_id)
                         AND ${dateClause('r')}`;

  const [orgTotals, usersByRole, referralTotals, feedbackTotals, orgGrowth, orgStatus] = await Promise.all([
    query(
      `SELECT COUNT(*) AS total_organisations,
              SUM(CASE WHEN type = 'NGO' THEN 1 ELSE 0 END) AS total_ngos,
              SUM(CASE WHEN type = 'HOSPITAL' THEN 1 ELSE 0 END) AS total_hospitals,
              SUM(CASE WHEN status = 'ACTIVE' THEN 1 ELSE 0 END) AS active_organisations,
              SUM(CASE WHEN status != 'ACTIVE' THEN 1 ELSE 0 END) AS inactive_organisations
       FROM organisations o
       WHERE ${orgWhere}`,
      params
    ),
    query(
      `SELECT u.role, COUNT(*) AS total_users
       FROM users u
       LEFT JOIN organisations o ON o.id = u.organisation_id
       WHERE (:organisation_id IS NULL OR u.organisation_id = :organisation_id)
         AND (:start_date IS NULL OR DATE(u.created_at) >= :start_date)
         AND (:end_date IS NULL OR DATE(u.created_at) <= :end_date)
       GROUP BY u.role
       ORDER BY total_users DESC`,
      params
    ),
    query(`SELECT COUNT(*) AS total_referrals FROM referrals r WHERE ${referralWhere}`, params),
    query(
      `SELECT COUNT(f.id) AS total_feedback
       FROM feedback f
       JOIN referrals r ON r.id = f.referral_id
       WHERE ${referralWhere}`,
      params
    ),
    query(
      `SELECT DATE_FORMAT(o.created_at, '%Y-%m') AS month, o.type AS organisation_type, COUNT(*) AS organisations_registered
       FROM organisations o
       WHERE ${orgWhere}
       GROUP BY DATE_FORMAT(o.created_at, '%Y-%m'), o.type
       ORDER BY month`,
      params
    ),
    query(
      `SELECT o.status AS organisation_status, COUNT(*) AS total
       FROM organisations o
       WHERE ${orgWhere}
       GROUP BY o.status`,
      params
    )
  ]);

  const totals = orgTotals[0] || {};
  const totalUsers = usersByRole.reduce((sum, row) => sum + Number(row.total_users || 0), 0);
  const summaryCards = cards([
    ['Total NGOs', totals.total_ngos],
    ['Total hospitals', totals.total_hospitals],
    ['Total organisations', totals.total_organisations],
    ['Active organisations', totals.active_organisations],
    ['Inactive organisations', totals.inactive_organisations],
    ['Total users', totalUsers],
    ['Total referrals', referralTotals[0]?.total_referrals],
    ['Total feedback records', feedbackTotals[0]?.total_feedback]
  ]);

  return {
    summary_cards: summaryCards,
    charts: [
      { title: 'Organisations by type', rows: [{ label: 'NGO', total: totals.total_ngos || 0 }, { label: 'Hospital', total: totals.total_hospitals || 0 }] },
      { title: 'Users by role', rows: usersByRole.map((row) => ({ label: row.role, total: row.total_users })) },
      { title: 'Organisation status', rows: orgStatus.map((row) => ({ label: row.organisation_status, total: row.total })) }
    ],
    sections: [
      section('High-level system metrics', summaryCards.map((card) => ({ metric: card.label, value: card.value }))),
      section('Users by role', usersByRole),
      section('Organisation growth trend', orgGrowth)
    ],
    observations: [
      totalUsers ? insight(`${totalUsers} user account(s) matched the selected filters.`) : insight('No users matched the selected filters.'),
      referralTotals[0]?.total_referrals ? insight('Referral activity exists for the selected period.') : insight('No referrals were recorded in the selected period.')
    ]
  };
}

