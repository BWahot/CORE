async function generateReportData(report, user, filters) {
  if (report.report_key === 'platform_system_overview') return platformSystemOverview(user, filters);
  if (report.report_key === 'platform_organisation') return platformOrganisationReport(user, filters);
  if (report.report_key === 'organisation_activity') return organisationActivityReport(user, filters);
  if (report.report_key === 'ngo_social_worker_activity') return ngoSocialWorkerReport(user, filters);
  if (report.report_key === 'hospital_records_keeper_activity') return hospitalRecordsKeeperReport(user, filters);
  return { summary_cards: [], charts: [], sections: [], observations: [] };
}

async function buildReportResponse(report, user, filters, outputFormat) {
  const generatedAt = new Date().toISOString();
  const data = await generateReportData(report, user, filters);
  const summaryCards = normaliseSummaryCards(data.summary_cards);
  const response = {
    report_key: report.report_key,
    report_name: report.report_name,
    generated_at: generatedAt,
    generated_by: generatedBy(user),
    filters_applied: selectedFilters(filters, user),
    output_format: outputFormat,
    summary_cards: summaryCards,
    charts: data.charts,
    sections: data.sections,
    observations: buildObservations(summaryCards, data.observations),
    summary: { total_records: data.sections.reduce((sum, item) => sum + (item.rows?.length || 0), 0) },
    rows: data.sections[0]?.rows || []
  };

  if (outputFormat === 'csv') {
    Object.assign(response, csvPayload(response));
  }

  if (outputFormat === 'pdf') {
    Object.assign(response, pdfPayload(response));
  }

  return response;
}

