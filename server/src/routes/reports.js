function selectedFilters(filters, user) {
  const selected = {
    date_range: filters.start_date || filters.end_date ? `${filters.start_date || 'Beginning'} to ${filters.end_date || 'Today'}` : 'All time'
  };

  if (user.role === ROLES.PLATFORM_ADMIN) {
    selected.organisation_filter = filters.organisation_id || 'All organisations';
  } else {
    // Operational and organisation-admin reports are always constrained by the authenticated user's organisation.
    selected.organisation_scope = user.organisation_name || 'Assigned organisation';
  }

  return selected;
}

