async function organisationActivityReport(user, filters) {
  const params = filterParams(filters, user);
  const sideColumn = user.organisation_type === 'NGO' ? 'r.referring_organisation_id' : 'r.receiving_organisation_id';

  const [profile, staff, referralStatus, referralTrend, referrals, feedback, performance] = await Promise.all([
    query('SELECT name, type, email, phone, location, status, DATE(created_at) AS date_registered FROM organisations WHERE id = :current_organisation_id', params),
    query(
      `SELECT full_name, email, role, status, DATE(created_at) AS date_added
       FROM users
       WHERE organisation_id = :current_organisation_id
         AND (:start_date IS NULL OR DATE(created_at) >= :start_date)
         AND (:end_date IS NULL OR DATE(created_at) <= :end_date)
       ORDER BY full_name`,
      params
    ),
    query(
      `SELECT r.status, COUNT(*) AS total
       FROM referrals r
       WHERE ${sideColumn} = :current_organisation_id AND ${dateClause('r')}
       GROUP BY r.status`,
      params
    ),
    query(
      `SELECT DATE_FORMAT(r.created_at, '%Y-%m') AS month, COUNT(*) AS total
       FROM referrals r
       WHERE ${sideColumn} = :current_organisation_id AND ${dateClause('r')}
       GROUP BY DATE_FORMAT(r.created_at, '%Y-%m')
       ORDER BY month`,
      params
    ),
    query(
      `SELECT r.referral_number, b.case_number, ref_org.name AS referred_by, rec_org.name AS referred_to,
              r.status, DATE(r.created_at) AS referral_date,
              CASE WHEN f.id IS NULL THEN 'PENDING' ELSE 'SUBMITTED' END AS feedback_status
       FROM referrals r
       JOIN beneficiaries b ON b.id = r.beneficiary_id
       JOIN organisations ref_org ON ref_org.id = r.referring_organisation_id
       JOIN organisations rec_org ON rec_org.id = r.receiving_organisation_id
       LEFT JOIN feedback f ON f.referral_id = r.id
       WHERE ${sideColumn} = :current_organisation_id AND ${dateClause('r')}
       ORDER BY r.created_at DESC
       LIMIT 100`,
      params
    ),
    query(
      `SELECT COUNT(f.id) AS total_feedback,
              SUM(CASE WHEN f.id IS NULL AND r.status = 'Completed' THEN 1 ELSE 0 END) AS pending_feedback,
              SUM(CASE WHEN f.id IS NOT NULL THEN 1 ELSE 0 END) AS submitted_feedback
       FROM referrals r
       LEFT JOIN feedback f ON f.referral_id = r.id
       WHERE ${sideColumn} = :current_organisation_id AND ${dateClause('r')}`,
      params
    ),
    query(
      `SELECT SUM(CASE WHEN r.status IN ('Pending', 'Accepted', 'In Progress') THEN 1 ELSE 0 END) AS pending_actions,
              SUM(CASE WHEN r.status = 'Completed' THEN 1 ELSE 0 END) AS completed_referrals,
              SUM(CASE WHEN r.status = 'Rejected' THEN 1 ELSE 0 END) AS rejected_referrals,
              ROUND(AVG(CASE
                WHEN COALESCE(r.completed_at, r.accepted_at, r.updated_at) >= r.created_at
                THEN TIMESTAMPDIFF(HOUR, r.created_at, COALESCE(r.completed_at, r.accepted_at, r.updated_at))
                ELSE NULL
              END), 1) AS average_response_hours
       FROM referrals r
       WHERE ${sideColumn} = :current_organisation_id AND ${dateClause('r')}`,
      params
    )
  ]);

  const feedbackSummary = feedback[0] || {};
  const performanceSummary = performance[0] || {};
  return {
    summary_cards: cards([
      ['Staff users', staff.length],
      ['Total referrals', referrals.length],
      ['Feedback records', feedbackSummary.total_feedback],
      ['Pending actions', performanceSummary.pending_actions],
      ['Completed referrals', performanceSummary.completed_referrals],
      ['Rejected referrals', performanceSummary.rejected_referrals],
      ['Average response hours', performanceSummary.average_response_hours ?? 'Not available']
    ]),
    charts: [
      { title: 'Referral statuses', rows: referralStatus.map((row) => ({ label: row.status, total: row.total })) },
      { title: 'Referrals over time', rows: referralTrend.map((row) => ({ label: row.month, total: row.total })) },
      { title: 'Feedback status', rows: [{ label: 'Pending', total: feedbackSummary.pending_feedback || 0 }, { label: 'Submitted', total: feedbackSummary.submitted_feedback || 0 }] }
    ],
    sections: [
      section('Organisation profile summary', profile),
      section('Staff users', staff),
      section('Referral summary', referrals),
      section('Feedback summary', [feedbackSummary]),
      section('Performance summary', [performanceSummary])
    ],
    observations: [
      referrals.length ? insight(`${referrals.length} referral(s) matched the selected period.`) : insight('No referrals were recorded in the selected period.'),
      Number(feedbackSummary.pending_feedback || 0) > 0 ? insight('Some completed referrals still have pending feedback.') : insight('No completed referrals are waiting for feedback.')
    ]
  };
}

